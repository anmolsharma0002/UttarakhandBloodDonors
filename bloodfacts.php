2. The need for blood increases by 5% every year.<br>

3. 75% donors donate only once a year.  If they donated twice a year, there will never be any shortage of blood.<br>

4. Blood can be donated by any volunteer, a person from among us, who makes the choice to donate blood. When you donate blood, you join a very select group, currently only 0.8 out of every 100 people in India donate blood.You have the power to make a difference in someone's life. All you have to do is make a contribution. Every donation helps to save 3-4 lives, your contributions can help bring comfort to those in need.
</p>


<label>Facts about Donors</label>
<p>
1. The number one reason donors say they give blood is because they "want to help others."<br>
2. Two most common reasons cited by people who don't give blood are: "Never thought about it" and "I don't like needles."<br>
3. One donation can help save the lives of up to three people.<br>
4. If you began donating blood at age 18 and donated every 90 days until you reached 60, you would have donated 30 gallons of blood, potentially helping save more than 500 lives!.<br>
5. Only 7 percent of people in India have O-negative blood type. O-negative blood type donors are universal donors as their blood can be given to people of all blood types.<br>
6. Type O-negative blood is needed in emergencies before the patient's blood type is known and with newborns who need blood.<br>
7. Thirty-five percent of people have Type O (positive or negative) blood.<br>
8. 0.4 percent of people have AB-blood type. AB-type blood donors are universal donors of plasma, which is often used in emergencies, for newborns and for patients requiring massive transfusions.
