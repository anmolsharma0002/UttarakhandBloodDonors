3.  Coconut water can be used (in emergencies) as a substitute for blood plasma.<br>
4.  Pregnant women have about 50% more blood by week 20 of pregnancy than they did before they conceived.<br>
5.   Blood consists of a yellow liquid which is known as blood plasma.<br>
6.  James Harrison has donated blood over 1,000 times, saving over 2 million unborn babies from Rhesus disease.<br>
7.  Red blood cells develop in bone marrow and circulate in the body for around 120 days.<br>
8.  Donors can give either whole blood or specific blood components only. The process of donating specific blood components – red cells, plasma or platelets – is called apheresis.<br>
9.  Donated platelets must be used within five days of collection.<br>
10. One transfusion dose of platelets can be obtained through one apheresis donation of platelets or by combining the platelets derived from five whole blood donations.<br>
11. An adult body has 100,000 kilometers or 60,000 miles of blood vessels running throughout the body.<br>
12. Though we are familiar with common blood types A, B, AB and O, which is a part of simplified ABO system, there are actually around 30 different recognized blood groups or blood types.<br>
13. Blood is not just responsible for carrying oxygen to different parts of the body.It also carries nutrients to body cells.At the same time,it also carries away unwanted waste material away from the cells.<br>
14. Only the red blood cells in our blood are responsible for carrying oxygen.<br>
15. Platelets are completely different and are responsible for blood clotting whenever bleeding occurs because of a cut or bruise.This prevents unwanted loss of blood.<br>
16. The average volume of blood present in an adult male body is 5.6 liters while an adult female body contains 4.5 liters of blood on an average.<br>
17. Average blood pressure will always vary from one person to another despite the fact that  standard considered BP is 112/64 mmHg.<br>
18. High BP implies increased risk of stroke and heart attack.<br>
19. Blood pressure is actually the pressure exerted by blood on blood vessel walls. BP is  a vital sign of life.<br>
20. Platelets, white blood cells and red blood cells are all found floating in blood plasma.<br>
</p>


<label>Who can donate !</label>
<p>
1. You are between age group of 18-60 years.<br>
2. Your weight is 45 kgs or more.<br>
3. Your haemoglobin is 12.5 gm% minimum.<br>
4. Your last blood donation was 3 months earlier.<br>
5. You are healthy and have not suffered from malaria, typhoid or other transmissible disease in the recent past.<br>
6. Pulse between 100-180/min.,regular.<br>
7. Temperature(oral)-not more than 37.5 degree celcius.<br>
8. Blood pressure- Systolic : 100-180mmHG , Diastolic : 50-100mmHG.
</p>

<label>Who can't donate</label> 

<p>
<font style="font-family:arial black">Do not donate blood if you have any of these conditions: </font><br>
1. Cold / fever in the past 1 week.<br>
2. Major surgery in the last 6 months.<br>
3. Cardiac problems, hypertension, epilepsy, diabetes (on insulin therapy), history of cancer,chronic kidney or liver disease, bleeding tendencies, venereal disease etc. <br>
4. Under treatment with antibiotics or any other medication.<br>
5. Been tested positive for antibodies to HIV.<br>
6. Had a miscarriage in the last 6 months or have been pregnant / lactating in the last one year. <br>
7. Had fainting attacks during last donation.<br>
8. Have regularly received treatment with blood products.<br>
9. Females should not donate blood during pregnancy.<br>
10.Females can donate after 6 months following a normal delivery and when they are not breast feeding. <br>
11.Females should not donate blood if they are having heavy menstrual flow or menstrual cramps.<br>
12.Shared a needle to inject drugs/ have history of drug addiction. <br>
13.Had sexual relations with different partners or with a high risk individual.<br>
14.Vaccination in the last 24 hours.
</p>


<label>Why Donate Blood?</label>

<p>
1. As you know blood cannot be harvested it can only be donated. This means only you can save a life that needs blood.<br>

2. Every year India requires 40 million units of 250cc blood out of which only a meager 500,000 of blood units are available.<br>

3. Saving a life does not require heroic deeds. You could just do it with a small thought and an even smaller effort for saying "yes".<br>

4. Blood is the living fluid that all life is based on.<br>

5. Blood is composed of 60% liquid part and 40% solid part.<br>

6. The liquid part called Plasma, made up of 90% water and 10% nutrients, hormones, etc. is easily replenished by food, medicines, etc. But the solid part that contains RBC (red blood cells), WBC (white blood cells) and Platelets take valuable time to be replaced if lost.<br>
 
7. One out of every three will need blood in our life time.<br>

8. There is no substitute for Blood.<br>

9. Every two seconds, someone needs blood.<br>

10.350 ml of blood can save three lives.<br>
</p>


